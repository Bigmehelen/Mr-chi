public class User {
    private String name;
    private String age;
    private String email;
    private String phoneNumber;
    private String homeAddress;

    public User(String name, String age, String email, String phoneNumber, String homeAddress) {
        this.name = name;
        this.age = age;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.homeAddress = homeAddress;
    }


    public User() {
    }
}
